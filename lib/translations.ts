export type Language = keyof typeof translations
export type TranslationKey = keyof typeof translations.zh

// 翻译函数
export function useTranslation(lang: Language) {
  return (key: TranslationKey, params?: Record<string, string | number>): string => {
    let text = translations[lang][key] || translations.zh[key] || key
    
    // 处理参数替换
    if (params) {
      Object.entries(params).forEach(([param, value]) => {
        text = text.replace(`{${param}}`, String(value))
      })
    }
    
    return text
  }
}

// 从请求头检测语言偏好
export function detectLanguageFromRequest(request: Request): Language {
  const acceptLanguage = request.headers.get('accept-language') || ''
  
  // 检查是否包含中文
  if (acceptLanguage.includes('zh')) {
    return 'zh'
  }
  
  // 默认返回英文
  return 'en'
}

// 服务端翻译函数
export function translateForRequest(request: Request, key: TranslationKey, params?: Record<string, string | number>): string {
  const lang = detectLanguageFromRequest(request)
  const t = useTranslation(lang)
  return t(key, params)
}